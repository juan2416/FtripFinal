package ale.juan.evento;

public class evento {
	
	private String all_members_count; 
	private String description; 
    private Long eid; 
    private String pic_square; 
    private String name; 
    private String privacy; 
    private String start_time;
	
     /**
	 * @return the all_members_count
	 */
	public String getAll_members_count() {
		return all_members_count;
	}
	/**
	 * @param all_members_count the all_members_count to set
	 */
	public void setAll_members_count(String all_members_count) {
		this.all_members_count = all_members_count;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the eid
	 */
	public Number getEid() {
		return eid;
	}
	/**
	 * @param eid the eid to set
	 */
	public void setEid(Long eid) {
		this.eid = eid;
	}
	/**
	 * @return the pic_square
	 */
	public String getPic_square() {
		return pic_square;
	}
	/**
	 * @param pic_square the pic_square to set
	 */
	public void setPic_square(String pic_square) {
		this.pic_square = pic_square;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the privacy
	 */
	public String getPrivacy() {
		return privacy;
	}
	/**
	 * @param privacy the privacy to set
	 */
	public void setPrivacy(String privacy) {
		this.privacy = privacy;
	}
	/**
	 * @return the start_time
	 */
	public String getStart_time() {
		return start_time;
	}
	/**
	 * @param start_time the start_time to set
	 */
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
}
